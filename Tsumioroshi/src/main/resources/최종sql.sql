CREATE TABLE STOCK_LOG 
(
  PRODUCT_NAME VARCHAR2(20 CHAR) NOT NULL 
, PRODUCT_NUMBER VARCHAR2(12 CHAR) 
, ORDER_NUMBER NUMBER NOT NULL 
, ORDER_DATE DATE NOT NULL 
, LOG_DATE DATE DEFAULT sysdate NOT NULL 
, DEPARTURE VARCHAR2(100 CHAR) NOT NULL 
, STOCK_AMOUNT NUMBER NOT NULL 
, CHARGE1 VARCHAR2(20 CHAR) NOT NULL 
, CHARGE2 VARCHAR2(20 CHAR) 
, CONFIRM_DATE DATE DEFAULT sysdate 
);

CREATE TABLE SHELF 
(
  SHELF_NAME VARCHAR2(20 CHAR) NOT NULL 
, SHELF_SIZE VARCHAR2(40 CHAR) NOT NULL 
, SHELF_LOCATION VARCHAR2(40 CHAR) 
, SHELF_CONDITION CHAR(1 BYTE) DEFAULT '1' 
, CONSTRAINT SYS_C007498 PRIMARY KEY 
  (
    SHELF_NAME 
  )
);

CREATE TABLE SECOND_CATEGORY 
(
  FC_CODE VARCHAR2(20 CHAR) 
, SC_CODE VARCHAR2(20 CHAR) NOT NULL 
, SC_NAME VARCHAR2(20 CHAR) NOT NULL 
);

CREATE TABLE RELEASE_LOG 
(
  PRODUCT_NAME VARCHAR2(20 CHAR) NOT NULL 
, PRODUCT_NUMBER VARCHAR2(12 CHAR) 
, ORDER_NUMBER NUMBER NOT NULL 
, ORDER_DATE DATE NOT NULL 
, LOG_DATE DATE DEFAULT sysdate NOT NULL 
, DESTINATION VARCHAR2(100 CHAR) NOT NULL 
, DELIVERY_AMOUNT NUMBER NOT NULL 
, CHARGE1 VARCHAR2(20 CHAR) NOT NULL 
, CHARGE2 VARCHAR2(20 CHAR) 
, CONFIRM_DATE DATE DEFAULT sysdate 
);

CREATE TABLE PRODUCT 
(
  PRODUCT_NAME VARCHAR2(20 CHAR) NOT NULL 
, PRODUCT_NUMBER VARCHAR2(12 CHAR) 
, TAG_NUMBER VARCHAR2(20 CHAR) NOT NULL 
, QUANTITY NUMBER NOT NULL 
, EXPIRATION_DATE DATE 
, STOCK_DATE DATE DEFAULT sysdate NOT NULL 
, PRODUCT_LOCATION VARCHAR2(7 CHAR) NOT NULL 
);

CREATE TABLE MESSAGE 
(
  MESSAGE_NO NUMBER NOT NULL 
, ID VARCHAR2(20 CHAR) 
, PASSWORD VARCHAR2(20 CHAR) NOT NULL 
, TITLE VARCHAR2(20 CHAR) NOT NULL 
, REGDATE DATE DEFAULT sysdate NOT NULL 
, CONTENT VARCHAR2(1000 CHAR) NOT NULL 
, CONFIRM VARCHAR2(20 CHAR) 
, USER_TO VARCHAR2(20 CHAR) 
, CONFIRM_DATE DATE 
, CONSTRAINT SYS_C007214 PRIMARY KEY 
  (
    MESSAGE_NO 
  )
);

CREATE TABLE MEMBERS 
(
  ID VARCHAR2(20 CHAR) NOT NULL 
, PASSWORD VARCHAR2(20 CHAR) NOT NULL 
, MASTERPW VARCHAR2(20 CHAR) 
, AUTHORITY VARCHAR2(20 CHAR) 
, CONSTRAINT MEMBERS_ID_PK PRIMARY KEY 
  (
    ID 
  )
